import mongoose from 'mongoose';

const handleCastError = (err: mongoose.Error.CastError) => {
  const errorSources = [
    {
      path: err.path,
      message: err.message,
    },
  ];

  const statusCode = 400;

  return {
    statusCode,
    message: 'Invalid ID',
    errorSources,
  };
};

export default handleCastError;
